                    <p>ここ10年以上は故郷の福岡を拠点に、東京など各地でライヴ活動を展開、様々なコンテストでも栄冠を獲得する等、その力量をファンに強く印象付けている、実力派の山口葵。その彼女が数年前に単身NYに渡り、職人肌のピアニスト、ビル・メイズと共に作り上げた2枚のアルバムは、等身大の彼女を映し取った、実にチャーミングな佳品だった。
そして今回彼女が共演に選んだのは、現在最も充実しているピアニストの大石学(アレンジも担当)と、ベースの隠れ名手、山村隆一の2人。シンプルな編成だが、その声が最高に引き立つ絶妙なセレクションで、しっとりと落ち着きがあり、しかも軽やかで伸びやかでもある、その旨みある歌い口が沁みる。「少しの無理も無駄もない」と、前作で湯川れい子さんが評した、その巧みな歌唱はここでも健在で、更に表現の深さと表情の豊tかさを増し、ファンを魅する。最近のヴォーカル作品の中でも、この心地良さ、そうはない。伴奏と編曲の大石も、ハモンド等も駆使し頗（すこぶ）る快調で、ピアニカを併用した⑧の哀愁が心憎い。山村と彼女の2曲のデュエットもグッド。（小西啓一）<br><br>■①マイ・ロマンス ②ドリーミー ③ティーチ・ミー・トゥナイト ④ウィスパー・ノット　⑤オールモースト・ライク・ビーング・イン・ラヴ ⑥ザ・ルック・オブ・ラヴ ⑦アイヴ・ガット・ユー・アンダー・マイ・スキン ⑧エスタテ ⑨ジャスト・ワン・オブ・ゾーズ・シングズ　⑩ゴンドラの歌　⑪ラヴァー、カム・バック・トゥミー</p>
