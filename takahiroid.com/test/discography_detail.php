                    <p>福岡生まれで福岡を拠点に活動しているヴォーカリスト山口葵の新作。うしじまあおい名義で1枚、NYのビル・メイズを招いて2枚と実績があるだけでなく、岡崎や浅草など、各地のヴォーカル・コンテストでグランプリ受賞など、高い評価を得ている実力派の新境地は、ピアノとベースだけというシンプルなサウンドの中で、自らの声の表情が、どれだけ曲の核心に近づくことができるのかへのチャレンジのように映る。それだけ歌い手として丸裸になれるのは、天性の声質と磨き上げたテクニックがあるゆえだが、上手さへ身を預けない姿勢こそが本作の真骨頂。だからこそシブい伴奏でも浮かずに心に響くのだ。（富澤えいち）</p>
